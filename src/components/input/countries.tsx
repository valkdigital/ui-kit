const putFlagInTextComponent = (
  countries: Record<CountryCodes, CountryData>
) => {
  return Object.entries(countries).reduce(
    (result: Record<string, Option>, [key, value]) => {
      const { flag, ...data } = value;
      result[key] = {
        ...data,
        leftComponent: (
          <Text style={styles.flag} type="h4">
            {flag}
          </Text>
        ),
      };
      return result;
    },
    {}
  ) as Record<CountryCodes, Option>;
};

